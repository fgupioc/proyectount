    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (!txtHost.getText().equals("")) {
            if (!txtDataBase.getText().equals("")) {
                if (!txtUsuario.getText().equals("")) {
                    try {
                        GestionarConfiguracionServicio conf = new GestionarConfiguracionServicio();
                        Configuracion dts = new Configuracion();
                        dts.setHost(txtHost.getText().trim());
                        dts.setDb(txtDataBase.getText().trim());
                        dts.setUser(txtUsuario.getText().trim());
                        dts.setPass(txtPassword.getText().trim());
                        if(conf.guardar(dts)){
                            JOptionPane.showMessageDialog(this,"La configuracion se Guardo");
                            if(conf.validarConexion()){
                                FormLogin form = new FormLogin();
                                form.setVisible(true);
                                this.dispose();
                              }else{ 
                                JOptionPane.showMessageDialog(null,"Problemas al conectarse.. ingrese otra vez lo datos","Erros en las credenciales",0);
                            } 
                        }else{
                            JOptionPane.showMessageDialog(null,"No se pudo guardad las credenciales","Erros",2);
                        }
                    } catch(Exception e) { 
                       JOptionPane.showMessageDialog(this,e);
                    } 
                } else {
                    JOptionPane.showMessageDialog(this, "Debe llenar el Usuario", "Aviso", 0);
                    txtUsuario.requestFocus();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Debe llenar el nombre de la Base de Datos", "Aviso", 0);
                txtDataBase.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Debe llenar el Host", "Aviso", 0);
            txtHost.requestFocus();
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

